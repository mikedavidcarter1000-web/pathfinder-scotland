export function SocialLoginButtons({ redirectTo }: SocialLoginButtonsProps) {
  const oauthSignIn = useOAuthSignIn()

  const handleSocialLogin = (provider: OAuthProvider) => {
    oauthSignIn.mutate({ provider, redirectTo })
  }

  return (
    <div className="space-y-3">
      {providers.map((provider) => (
        <button
          key={provider.id}
          type="button"
          onClick={() => handleSocialLogin(provider.id)}
          disabled={oauthSignIn.isPending}
          className={`w-full flex items-center justify-center gap-3 py-2.5 px-4 rounded-lg font-medium transition-colors disabled:opacity-50 ${provider.bgColor} ${provider.textColor}`}
        >
          {provider.icon}
          <span>Continue with {provider.name}</span>
        </button>
      ))}
    </div>
  )
}

